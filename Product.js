class Product{
id;
name;
image;
subImage;
salePrice;
realPrice;
release;
discount;
constructor(id,name,image,subImage,salePrice,realPrice,release,discount) {
    this.id = id;
    this.name = name;
    this.image = image;
    this.subImage = subImage;
    this.salePrice = salePrice;
    this.realPrice = realPrice;
    this.release = release;
    this.discount = discount;
}
}
